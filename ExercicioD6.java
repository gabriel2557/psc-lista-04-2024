public class ExercicioD6{
    public static void main(String[] args) {
        // Imprimir os números um abaixo do outro
        System.out.println("Números de 1 a 20 abaixo um do outro:");
        for (int i = 1; i <= 20; i++) {
            System.out.println(i);
        }
        
        // Imprimir os números um ao lado do outro
        System.out.println("\nNúmeros de 1 a 20 ao lado um do outro:");
        for (int i = 1; i <= 20; i++) {
            System.out.print(i + " ");
        }
    }
}